    private void newUserToFile(Createuser Createuseaccount) {
        try (PrintWriter writer = new PrintWriter(new FileWriter("login.txt",true))) {
            writer.println(Createuseaccount.getUsername() + ":" + Createuseaccount.getPassword());
        } catch (IOException e) {
            e.printStackTrace(); 
            JOptionPane.showMessageDialog(this, "Error adding user", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    } 
